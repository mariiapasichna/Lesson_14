package com.javaelementary;

public class Calculator {
    public static double sum(int a, int b) {
        return a + b;
    }

    public static double diff(int a, int b) {
        return a - b;
    }

    public static double mult(int a, int b) {
        return a * b;
    }

    public static double div(int a, int b) {
        return (double) a / b;
    }
}